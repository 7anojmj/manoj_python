
print("New line is \\n")
txt = "Twinkle Twinkle \n Little Star"
print(txt)

print("Quotes is \"")
txt = "Twinkle Twinkle \"Little\" Star"
print(txt)

print("Single Quotes is \'")
txt = "Twinkle Twinkle \'Little\' Star"
print(txt)


print("Tab Character\\t")
txt = "Twinkle Twinkle Little Star"
print(txt)
txt = "Twinkle Twinkle \tLittle Star"
print(txt)

print("Backspace Character\\b")
txt = "Twinkle\b Twin\bkle Little Star\b"
print(txt)


# Code	Result	Try it
# \'	Single Quote
# \\	Backslash
# \n	New Line
# \r	Carriage Return
# \t	Tab
# \b	Backspace
# \f	Form Feed
# \ooo	Octal value
# \xhh	Hex value
